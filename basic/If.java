package kosa.basic;

public class If {

	public static void main(String[] args) {
		// if
		
		int num1 = 2;
        int num2 = 24;
        if (num1 > num2) {
            System.out.println("num1 값이 더 큽니다.");
            System.out.println("num1 = " + num1);
        } else {
        	System.out.println("num1 값이 더 작습니다.");
            System.out.println("num2 = " + num2);
        }
        System.out.println("Done.");
    }

	

}
